/**
 * El alumnado y el profesorado se identifican con un alias en el sistema y se comunican a través de correo electrónico;
 * Por motivos administrativos es necesario  conocer el nombre y apellidos, dirección completa y teléfono de todas las 
 * personas que participan en el sistema, sea como  profesores o como alumnos.
 * @author madrid INESTORDOYALAGUNA
 */



public class Persona {

    
    //GETTERS Y SETTERS DE NUESTRA CLASE PERSONA
    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the direccion
     */
    public String getDireccion() {
        return direccion;
    }

    /**
     * @param direccion the direccion to set
     */
    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    /**
     * @return the telefono
     */
    public String getTelefono() {
        return telefono;
    }

    /**
     * @param telefono the telefono to set
     */
    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    /**
     * @return the alias
     */
    public String getAlias() {
        return alias;
    }

    /**
     * @param alias the alias to set
     */
    public void setAlias(String alias) {
        this.alias = alias;
    }

    /**
     * @return the apellidos
     */
    public String getApellidos() {
        return apellidos;
    }

    /**
     * @param apellidos the apellidos to set
     */
    public void setApellidos(String apellidos) {
        this.apellidos = apellidos;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    
    //ATRIBUTOS DE NUETSRA CLASE PERSONA
    private String nombre;

    private String direccion;

    private String telefono;

    private String alias;

    private String apellidos;

    private String email;
}
